public class Person {
    /**
     * 姓名
     */
    public String name;
    /**
     * 性别
     */
    public String sex;
    /**
     * 技能
     */
    public String skill;

    public Person() {

    }

    public Person(String name, String sex, String skill) {
        this.name = name;
        this.sex = sex;
        this.skill = skill;
    }
}
